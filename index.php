        function getQueryParams() {
            const params = {};
            window.location.search.substring(1).split("&").forEach(function(part) {
                const [key, value] = part.split("=");
                if (key) params[decodeURIComponent(key)] = decodeURIComponent(value);
            });
            return params;
        }

